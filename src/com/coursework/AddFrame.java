    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        try { //checking for nulls
            if (jTextFieldBookID.getText().isEmpty() || jTextFieldBookName.getText().isEmpty() || jTextFieldAuthor.getText().isEmpty()
                    || jTextFieldprice.getText().isEmpty()) //checking for null values
            {
                JOptionPane.showMessageDialog(this, "Fill all the text fields or select one option from edition", "Error!", JOptionPane.ERROR_MESSAGE);
            } else {
                if (Integer.parseInt(jTextFieldprice.getText()) < 1)//checking for zero or negative values
                {//checking for negative input
                    JOptionPane.showMessageDialog(this, "Please input positive integer in the field !!", "Error!", JOptionPane.ERROR_MESSAGE);
                } else {
                    String BookID = jTextFieldBookID.getText();
                    String BookName = jTextFieldBookName.getText();
                    String Category = (String) jComboBoxCategory.getSelectedItem();
                    int index = jComboBoxCategory.getSelectedIndex();
                    String Author = jTextFieldAuthor.getText();
                    String Price = jTextFieldprice.getText();
                    int rowCount = DataTecordTable.getRowCount();
                    int columnCount = DataTecordTable.getColumnCount();
                    int rowIndex = 0;
                    String Edition = "";
                    if (jRadioButtonNew.isSelected()) {
                        Edition = jRadioButtonNew.getText();
                    }
                    if (jRadioButtonOld.isSelected()) {
                        Edition = jRadioButtonOld.getText();
                    }

                    String[] values = {BookID, BookName, Category, Author, Edition, Price};
                    DefaultTableModel model = (DefaultTableModel) DataTecordTable.getModel();
                    model.addRow(values);
                }
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Enter valid input", "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_AddButtonActionPerformed

